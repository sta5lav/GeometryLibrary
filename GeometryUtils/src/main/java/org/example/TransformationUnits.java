package org.example;

public class TransformationUnits {

    //Conversion from square centimeters to square meters
    public static double squareCMToM(double d) {
        return d * 0.0001;
    }

    //Conversion from square meters to square centimeters
    public static double squareTToCM(double d) {
        return d / 0.0001;
    }




}
